# 1. Clean and extract only the unique target enums (ignores INVALID, MAX, and raw numbers)
target_enums = set()
for key in raw_enum_data.keys():
    if key.startswith("EM"):
        target_enums.add(key)

def count_enums_in_directory(directory_path):
    """Recursively counts exact occurrences of our target enums in a directory."""
    enum_counts = Counter()
    
    # We use \b (word boundary) to ensure we don't accidentally match parts of a longer string
    # like 'EM0001_00_0_EXTENDED' if it happens to exist.
    pattern = re.compile(r'\bEM\d{4}_\d{2}_\d\b')

    for root, _, files in os.walk(directory_path):
        for file in files:
            filepath = os.path.join(root, file)
            
            try:
                # Read the file contents (skipping unreadable/binary files)
                with open(filepath, 'r', encoding='utf-8') as f:
                    content = f.read()
                    
                    # Find all instances that look like our EM enums
                    matches = pattern.findall(content)
                    
                    # Only count the matches that exist in our target set
                    for match in matches:
                        if match in target_enums:
                            enum_counts[match] += 1
                            
            except (UnicodeDecodeError, PermissionError):
                # Safely ignore binary files like images/compiled code, or locked files
                pass

    return enum_counts

